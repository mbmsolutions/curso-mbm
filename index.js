app.post('/favoritos',(req,res) => {
    if(req.body.lista) {
        fs.writeFileSync("./favoritos.json", JSON.stringify(req.body.lista));
        res.send("Favoritos salvo com sucesso!");
    } else {
        res.send("Favoritos nÃ£o foram salvos!");
    }
})

